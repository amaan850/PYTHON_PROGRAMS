#1) is alnum():return true if all chracters are alphanumeric(letters or numbers)
s1="abc123"
print(s1.isalnum())
#2) is alpha():return true if all characters are alphabetic
s2="abc"
s3='123'
print(s2.isalpha())
print(s3.isalpha())
#3) is digit():return true if string contain only digit
print(s2.isdigit())
print(s3.isdigit())
#4) is lower(): return true if string contain only lower case
print(s2.islower())
print(s3.islower())
#5) is upper():return true if string contain all upper case chracters.
print(s2.isupper())
#6) is space(): return true if string is non empty and all characters are white space
s4="\n\t"
print(s4.isspace())
